def shorten(name: str) -> str:
    """Return brief display name for hospital."""
    if name in SPECIFIC:
        return SPECIFIC[name]
    n = name
    # 衛生福利部 → 部立
    n = re.sub(r"^衛生福利部", "部立", n)
    # Strip everything up to and including 醫療財團法人 / 醫療社團法人 / 社團法人 / 學校財團法人
    n = re.sub(r"^[^醫]*?(醫療財團法人|醫療社團法人|社團法人|學校財團法人)", "", n)
    # Strip leading 財團法人(私立)?
    n = re.sub(r"^財團法人(私立)?", "", n)
    # Strip 附設民眾診療服務處
    n = n.replace("附設民眾診療服務處", "")
    # Strip 及其XX院區 / 及其XX分院 suffix
    n = re.sub(r"及其\S+院區$", "", n)
    n = re.sub(r"及其\S+分院$", "", n)
    # Strip 委託 parenthetical clauses (both half-width and full-width parens)
    n = re.sub(r"[（(]委託.*?[）)]", "", n)
    # Strip -委託... suffix
    n = re.sub(r"[-－─]委託.+$", "", n)
    return n.strip()


def main():
    data = json.loads(GOV_BULK.read_text(encoding="utf-8"))
    out = {h["name"]: shorten(h["name"]) for h in data["hospitals"]}
    json.dump(out, sys.stdout, ensure_ascii=False, indent=2)
    sys.stdout.write("\n")


if __name__ == "__main__":
    main()
